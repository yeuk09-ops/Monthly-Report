import type { Metadata } from 'next';
import { Geist, Geist_Mono } from 'next/font/google';
import './globals.css';
import { Header } from '@/components/dashboard/Header';
import { ReportProvider } from '@/components/providers/ReportContext';
import { getAvailableMonths, getDefaultMonthData } from '@/lib/data';

const geistSans = Geist({
  variable: '--font-geist-sans',
  subsets: ['latin'],
});

const geistMono = Geist_Mono({
  variable: '--font-geist-mono',
  subsets: ['latin'],
});

export const metadata: Metadata = {
  title: 'FNF 경영실적 대시보드',
  description: 'F&F 월별 재무제표 대시보드',
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const index = await getAvailableMonths();
  const initialData = await getDefaultMonthData();

  return (
    <html lang="ko">
      <body className={`${geistSans.variable} ${geistMono.variable} antialiased bg-gradient-to-br from-slate-50 via-blue-50/30 to-indigo-50/20`}>
        <ReportProvider
          initialData={initialData}
          availableMonths={index.availableMonths}
          defaultMonth={index.defaultMonth}
        >
          <Header />
          <main className="min-h-screen">
            {children}
          </main>
          <footer className="bg-gradient-to-r from-indigo-900 via-blue-800 to-blue-700 text-blue-200 py-4 text-center text-sm">
            FNF Financial Dashboard v2.0 - Generated by Claude
          </footer>
        </ReportProvider>
      </body>
    </html>
  );
}
